package model;

/**
 * Classe que s'encarrega de guardar les posicions X i Y de la serp.
 *
 * Created by Grup 6 on 06/04/2017.
 */
public class Posicio {
    private float x;
    private float y;
    public Posicio(){

    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }

    public void setX(float x) {
        this.x = x;
    }

    public void setY(float y) {
        this.y = y;
    }
}
